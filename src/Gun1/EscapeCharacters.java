package Gun1;

public class EscapeCharacters
    {
        public static void main(String[] args)
        {
            /**
             * String(yazılar) için kullanacağımız özel karakterler.
             * \n -> yeni satırd
             * \t -> tab boşlupu oluşturur.
             * \" -> çift tırnak ekler
             * \' -> tek tırnak ekler
             * \\ -> ters slash ten bir tane ekler
             */
            System.out.println ("Satir1\tSatir2");
            System.out.println ("Ad\nSoyad");
            System.out.println ("O dedi ki: \"Merhaba Dünya\"");

        }

    }
